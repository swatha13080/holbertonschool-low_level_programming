/**
 * op_add - add two integers
 * @a: addend
 * @b: addend
 *
 * Return: sum
 */
int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract an integer from another
 * @a: minuend
 * @b: subtrahend
 *
 * Return: difference
 */
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: multiplicand
 * @b: multiplier
 *
 * Return: product
 */
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divide an integer by another
 * @a: dividend
 * @b: divisor
 *
 * Return: quotient
 */
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulo divide an integer by another
 * @a: dividend
 * @b: modulus
 *
 * Return: remainder
 */
int op_mod(int a, int b)
{
return (a % b);
}

